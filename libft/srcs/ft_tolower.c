#include "libft.h"

static int	ft_isupper(int c)
{
	if (c >= 65 && c <= 90)
		return (256);
	else
		return (0);
}

int	ft_tolower(int c)
{
	if (ft_isupper(c) == 256)
		c = c + 32;
	return (c);
}
